"""
이 문제를 푸는 핵심.
369게임이고, 369가 나오면 X가 출력되어야함.
3 % 10 = 3 -> 10으로 3을 나눌 수가 없으니 몫은 0, 나머지는 3.
3 % 10 = 3 -> 몫은 1, 나머지는 3
6 % 10 = 6 -> 몫은 0, 나머지 6

이런 방식으로 10으로 나눴을 떄 나머지가 3, 6, 9 인것을 걸러주면 됨.
"""

n = int(input())

for i in range(1, n+1):
    
    if (i % 10 == 3 or i % 10 == 6 or i % 10 == 9):
        print("X", end=" ")
        continue

    print(i, end=" ")